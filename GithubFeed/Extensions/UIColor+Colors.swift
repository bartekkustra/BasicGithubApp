import UIKit

extension UIColor {
    static func navyBlue() -> UIColor {
        return UIColor(red: 0.09, green: 0.11, blue: 0.13, alpha: 1.00)
    }
    
    static func lighterBlue() -> UIColor {
        return UIColor(red: 0.13, green: 0.15, blue: 0.18, alpha: 1.00)
    }
    
    static func ashen() -> UIColor {
        return UIColor(red: 0.79, green: 0.82, blue: 0.85, alpha: 1.00)
    }
    
    static func peach() -> UIColor {
        return UIColor(red: 0.93, green: 0.50, blue: 0.42, alpha: 1.00)
    }
    
    static func sun() -> UIColor {
        return UIColor(red: 0.96, green: 0.67, blue: 0.26, alpha: 1.00)
    }
}
